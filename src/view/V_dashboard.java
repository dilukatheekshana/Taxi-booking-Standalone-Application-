    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
       try {
    Connection con = MyDatabaseconnection.getMyConnection();
    Statement st = con.createStatement();
    ResultSet rs = st.executeQuery("SELECT * FROM vehical");
    
    // Create a DefaultTableModel and populate it with the ResultSet data
    DefaultTableModel model = new DefaultTableModel();
    model.setColumnIdentifiers(new Object[] {"Vehical_Code", "Vehical_Name", "Cost_per_Day"}); // Replace with your column names
    
    while (rs.next()) {
        // Extract the values from the ResultSet and add them to the model
        Object[] row = new Object[] {
            rs.getString("Vehical_Code"), 
            rs.getString("Vehical_Name"),
            rs.getString("Cost_per_Day")
        };
        model.addRow(row);
    }
    
    jTable1.setModel(model); // Set the model to your jTable
    
    rs.close();
    st.close();
    con.close();
} catch (Exception e) {
    JOptionPane.showMessageDialog(null, e.getMessage());
}

    }//GEN-LAST:event_formComponentShown

