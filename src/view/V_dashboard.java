    private void jMenuItem6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItem6ActionPerformed
        try {
            Connection con = model.MyDatabaseconnection.getMyConnection();
            String reportPath = "C:\\Users\\User\\Documents\\NetBeansProjects\\TAXI\\src\\view\\report1.jrxml";
            JasperDesign jdesign = JRXmlLoader.load(reportPath);
            String sqlQuery = "SELECT * FROM new_order";
            JRDesignQuery jquery = new JRDesignQuery();
            jquery.setText(sqlQuery);
            jdesign.setQuery(jquery);
            JasperReport report = JasperCompileManager.compileReport(jdesign);
            JasperPrint print = JasperFillManager.fillReport(report, null, con);
            JasperViewer.viewReport(print);
            con.close();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
        
        
    }//GEN-LAST:event_jMenuItem6ActionPerformed

